const physicsCards = [
    "光的反射", "光的折射", "光的直線傳播", "光的色散", 
    "分子擴散運動", "分子運動論", "物態變化", "熱傳遞", 
    "力的作用效果—形變", "槓桿和力矩平衡原理", "壓強", "浮力", 
    "牛頓第一定律", "牛頓第二定律", "牛頓第三定律", "相對運動", 
    "機械能守恆", "自由落體運動", "位能和動能", "機械振動", 
    "曲線運動", "動量守恆", "機械波", "波的繞射"
];

function PhysicsPoetryGame() {
    const [poemId, setPoemId] = useState("");
    const [selectedCard, setSelectedCard] = useState("");
    const [reasoning, setReasoning] = useState("");
    const [showExplanation, setShowExplanation] = useState(false);
    const [loading, setLoading] = useState(false);
    const [result, setResult] = useState(null);
    const [gameRecords, setGameRecords] = useState<any[]>([]);
    const [showRecordModal, setShowRecordModal] = useState(false);

    const handlePoemChange = (e: any) => {
        const id = e.target.value;
        setPoemId(id);
        setShowExplanation(false);
        setResult(null);
    };

    const handleSubmit = async () => {
    // 將 poemId 轉換為數字來作為索引
    const idAsNumber = parseInt(poemId);
    // 使用 @ts-ignore 暫時忽略這個特定的索引類型檢查，這是解決此類字典索引問題最快的方法，
    // 或者更嚴謹的做法是將 poems 定義為 Record<string, ...>，但這裡我們用轉型解決。
    // 其實最簡單的修復是直接斷言類型：
    if (!poems[idAsNumber as keyof typeof poems]) return alert('請選擇有效的詩句編號');
        if (!selectedCard) return alert('請選擇物理卡牌');
        if (!reasoning) return alert('請輸入解釋理由');

        setLoading(true);
        setResult(null);

        try {
            const response = await fetch('/api/check-physics', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    // 使用你剛剛轉換好的 idAsNumber，並同樣加上類型斷言
                    poem: poems[idAsNumber as keyof typeof poems].text, 
                    card: selectedCard,
                    reasoning
                }),
            });

            const data = await response.json();
            
            if (data.error) throw new Error(data.error);

            setResult(data);
            setGameRecords(prev => [...prev, {
                poem: poems[poemId].text,
                card: selectedCard,
                reasoning: reasoning,
                pass: data.pass ? "通過" : "失敗",
                score: data.score
            }]);

        } catch (error) {
            alert("判定錯誤: " + error.message);
        } finally {
            setLoading(false);
        }
    };

    const downloadExcel = () => {
        let csvContent = "\uFEFF"; 
        csvContent += "玩家,剩餘手牌\n";
        
        for(let i=1; i<=8; i++) {
            const name = document.getElementById(`p${i}_name`)?.value || "";
            const cards = document.getElementById(`p${i}_cards`)?.value || "";
            if(name) csvContent += `${name},${cards}\n`;
        }

        csvContent += "\n--- 遊戲判定記錄 ---\n";
        csvContent += "詩句,物理卡牌,解釋,結果,分數\n";
        gameRecords.forEach(rec => {
            const safeReason = rec.reasoning.replace(/"/g, '""');
            csvContent += `"${rec.poem}","${rec.card}","${safeReason}","${rec.pass}",${rec.score}\n`;
        });

        const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
        const link = document.createElement("a");
        const url = URL.createObjectURL(blob);
        link.setAttribute("href", url);
        link.setAttribute("download", "詩物遊戲記錄.csv");
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        setShowRecordModal(false);
    };

    return (
        <div className="min-h-screen bg-gray-50 p-4 font-sans text-gray-800">
            <div className="max-w-4xl mx-auto bg-white rounded-xl shadow-lg p-6">
                <h1 className="text-3xl font-bold text-center text-indigo-600 mb-8 flex items-center justify-center gap-2">
                    <BookOpen className="w-8 h-8" />
                    詩物遊戲 AI 裁判
                </h1>

                {/* 1. 詩句選擇 */}
                <div className="mb-8 p-6 bg-gray-50 rounded-lg border border-gray-200">
                    <h2 className="text-xl font-bold mb-4 flex items-center gap-2 text-gray-700">
                        <span className="bg-indigo-600 text-white w-8 h-8 rounded-full flex items-center justify-center text-sm">1</span>
                        詩句編號 (1-80)
                    </h2>
                    <input 
                        type="number" 
                        min="1" 
                        max="80" 
                        value={poemId}
                        onChange={handlePoemChange}
                        placeholder="輸入編號..."
                        className="w-full p-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 focus:border-indigo-500 outline-none"
                    />

                    {poems[poemId] && (
                        <div className="mt-4 p-4 bg-white border-l-4 border-indigo-500 rounded shadow-sm animate-fade-in">
                            <div className="text-xl font-bold text-gray-800 mb-2">{poems[poemId].text}</div>
                            <div className="text-gray-500 text-sm mb-4">— {poems[poemId].source}</div>
                            
                            <button 
                                onClick={() => setShowExplanation(!showExplanation)}
                                className="flex items-center gap-2 px-4 py-2 bg-emerald-500 text-white rounded-lg hover:bg-emerald-600 transition-colors text-sm"
                            >
                                <HelpCircle className="w-4 h-4" />
                                {showExplanation ? "隱藏釋義" : "查看釋義"}
                            </button>

                            {showExplanation && (
                                <div className="mt-4 p-4 bg-blue-50 text-blue-800 rounded-lg text-sm leading-relaxed">
                                    {explanations[poemId] || "暫無釋義"}
                                </div>
                            )}
                        </div>
                    )}
                </div>

                {/* 2. 物理卡牌 */}
                <div className="mb-8 p-6 bg-gray-50 rounded-lg border border-gray-200">
                    <h2 className="text-xl font-bold mb-4 flex items-center gap-2 text-gray-700">
                        <span className="bg-indigo-600 text-white w-8 h-8 rounded-full flex items-center justify-center text-sm">2</span>
                        物理卡牌
                    </h2>
                    <div className="grid grid-cols-2 md:grid-cols-4 gap-3">
                        {physicsCards.map(card => (
                            <button
                                key={card}
                                onClick={() => setSelectedCard(card)}
                                className={`p-3 rounded-lg text-sm font-medium transition-all ${
                                    selectedCard === card 
                                    ? 'bg-indigo-600 text-white shadow-md transform scale-105' 
                                    : 'bg-white text-gray-700 border border-gray-200 hover:border-indigo-300 hover:bg-indigo-50'
                                }`}
                            >
                                {card}
                            </button>
                        ))}
                    </div>
                </div>

                {/* 3. 解釋理由 */}
                <div className="mb-8 p-6 bg-gray-50 rounded-lg border border-gray-200">
                    <h2 className="text-xl font-bold mb-4 flex items-center gap-2 text-gray-700">
                        <span className="bg-indigo-600 text-white w-8 h-8 rounded-full flex items-center justify-center text-sm">3</span>
                        解釋理由
                    </h2>
                    <textarea 
                        value={reasoning}
                        onChange={(e) => setReasoning(e.target.value)}
                        rows="4"
                        placeholder="請說明物理原理是如何作用在詩句中的物體上..."
                        className="w-full p-3 border border-gray-300 rounded-lg focus:ring-2 focus:ring-indigo-500 outline-none mb-4"
                    />
                    <button 
                        onClick={handleSubmit}
                        disabled={loading}
                        className={`w-full py-3 rounded-lg text-white font-bold flex items-center justify-center gap-2 transition-all ${
                            loading ? 'bg-gray-400 cursor-not-allowed' : 'bg-indigo-600 hover:bg-indigo-700 shadow-md'
                        }`}
                    >
                        {loading ? <RefreshCw className="w-5 h-5 animate-spin" /> : <Send className="w-5 h-5" />}
                        {loading ? "AI 裁判思考中..." : "提交判定"}
                    </button>
                </div>

                {/* 結果顯示 */}
                {result && (
                    <div className={`mb-8 p-6 rounded-lg border-2 ${
                        result.pass ? 'bg-green-50 border-green-400' : 'bg-red-50 border-red-400'
                    }`}>
                        <div className="flex items-center gap-3 mb-2">
                            {result.pass ? (
                                <CheckCircle className="w-8 h-8 text-green-600" />
                            ) : (
                                <XCircle className="w-8 h-8 text-red-600" />
                            )}
                            <h3 className={`text-2xl font-bold ${result.pass ? 'text-green-800' : 'text-red-800'}`}>
                                {result.pass ? '判定通過' : '判定失敗'} 
                                <span className="ml-2 text-lg opacity-80">(分數: {result.score})</span>
                            </h3>
                        </div>
                        <p className={`text-lg ${result.pass ? 'text-green-700' : 'text-red-700'}`}>
                            {result.comment}
                        </p>
                    </div>
                )}

                {/* 底部控制 */}
                <div className="text-center">
                    <button 
                        onClick={() => setShowRecordModal(true)}
                        className="px-6 py-3 bg-gray-700 text-white rounded-lg hover:bg-gray-800 transition-colors flex items-center justify-center gap-2 mx-auto"
                    >
                        <Download className="w-5 h-5" />
                        遊戲結束 / 記錄成績
                    </button>
                </div>
            </div>

            {/* 記錄 Modal */}
            {showRecordModal && (
                <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50 p-4">
                    <div className="bg-white rounded-xl max-w-md w-full p-6 shadow-2xl">
                        <div className="flex justify-between items-center mb-6">
                            <h3 className="text-xl font-bold text-gray-800">📝 遊戲記錄</h3>
                            <button onClick={() => setShowRecordModal(false)} className="text-gray-400 hover:text-gray-600 text-2xl">&times;</button>
                        </div>
                        
                        <div className="max-h-[60vh] overflow-y-auto mb-6 pr-2">
                            {[...Array(8)].map((_, i) => (
                                <div key={i} className="flex gap-3 mb-3">
                                    <input 
                                        type="text" 
                                        id={`p${i+1}_name`} 
                                        placeholder={`玩家 ${i+1} 名字`}
                                        className="flex-1 p-2 border border-gray-300 rounded focus:border-indigo-500 outline-none"
                                    />
                                    <input 
                                        type="number" 
                                        id={`p${i+1}_cards`} 
                                        placeholder="剩餘手牌"
                                        className="w-24 p-2 border border-gray-300 rounded focus:border-indigo-500 outline-none"
                                    />
                                </div>
                            ))}
                        </div>

                        <button 
                            onClick={downloadExcel}
                            className="w-full py-3 bg-indigo-600 text-white rounded-lg hover:bg-indigo-700 font-bold"
                        >
                            提交記錄並生成 Excel
                        </button>
                    </div>
                </div>
            )}
        </div>
    );
}

export default PhysicsPoetryGame;